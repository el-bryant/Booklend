    private void btnPortadaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPortadaActionPerformed
        int resultado;
        PortadaAdd portada = new PortadaAdd();
        //establece filtro para cargar solo ese formato de archivos
        FileNameExtensionFilter filtro = new FileNameExtensionFilter("JPG y PNG", "jpg", "png");
        //establece el filtro en el JFileChooser
        portada.fchPortada.setFileFilter(filtro);
        //abre la ventana
        resultado = portada.fchPortada.showOpenDialog(null);
        if (JFileChooser.APPROVE_OPTION == resultado) {
            fichero = portada.fchPortada.getSelectedFile();
            try {
                ImageIcon icon = new ImageIcon(fichero.toString());
                //autoajuste al control que contendrá la imagen
                Icon icono = new ImageIcon(icon.getImage().getScaledInstance(lblPortada.getWidth(), lblPortada.getHeight(), Image.SCALE_DEFAULT));
                lblPortada.setText(null);
                lblPortada.setIcon(icono);
            } catch (Exception e) {
                JOptionPane.showMessageDialog(null,"Error abriendo la imagen" + e);
            }
        }
    }//GEN-LAST:event_btnPortadaActionPerformed

