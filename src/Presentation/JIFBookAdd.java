    public void cargareditoriales() {
        try {
            String sql = "SELECT nombre FROM editorial ORDER BY edit_id";
            PreparedStatement ps = cnn.getCnn().prepareStatement(sql);
            ResultSet rs = ps.executeQuery();
            while(rs.next()) {
                cboEditorial.addItem(rs.getString(1));
            }
        } catch (Exception e) {
            System.out.println(e.getMessage());
        }
    }
    
    public void cargarmaterias() {
        try {
            String sql = "SELECT nombre FROM materia ORDER BY nombre";
            PreparedStatement ps = cnn.getCnn().prepareStatement(sql);
            ResultSet rs = ps.executeQuery();
            while(rs.next()) {
                cboMateria.addItem(rs.getString(1));
            }
        } catch (Exception e) {
            System.out.println(e.getMessage().toString());
        }
    }
    
