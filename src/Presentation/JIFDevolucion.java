    private void setFilas(String dni) {
        try {
            String sql = "SELECT detalle_prestamo.detpres_id AS \"Código\", libro.lib_id AS \"Código de libro\", libro.titulo AS \"Título\", personal.apellidos || ' ' ||personal.nombres AS \"Personal\", to_char(prestamo.fentrega, 'YYYY/MM/DD') AS \"Fecha de entrega\", to_char(prestamo.fprestamo, 'YYYY/MM/DD') AS \"Fecha de préstamo\" FROM public.detalle_prestamo, public.libro, public.personal, public.prestamo WHERE detalle_prestamo.lib_id = libro.lib_id AND detalle_prestamo.pres_id = prestamo.pres_id AND prestamo.pers_dni = personal.pers_dni AND prestamo.usu_dni = '" + dni + "' AND detalle_prestamo.fdevolucion IS NULL";
            PreparedStatement ps = cnn.getCnn().prepareStatement(sql);
            ResultSet rs = ps.executeQuery();
            Object datos[] = new Object[6];
            while (rs.next()) {
                for (int i = 0; i < 6; i++) {
                    datos[i] = rs.getString(i + 1);
                }
                modelo.addRow(datos);
            }
            rs.close();
        } catch (SQLException e) {
            System.out.println(e.getMessage());
        }
    }
    
