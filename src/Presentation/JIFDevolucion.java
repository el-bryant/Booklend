    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
    try {
        Logic.DAOPrestamo obj = new Logic.DAOPrestamo();
        obj.setDetpres_id(Integer.parseInt(tbPrestamos.getValueAt(tbPrestamos.getSelectedRow(), 0).toString()));
        obj.setFdevolucion(ParseFecha(fechaActual()));
        obj.setLib_id(tbPrestamos.getValueAt(tbPrestamos.getSelectedRow(), 1).toString());
        if (obj.insertar_devolucion() == true) {
            JOptionPane.showMessageDialog(null, "Devolución registrada correctamente");
            dispose();
        } else {
            JOptionPane.showMessageDialog(null, "No se pudo registrar la devolución");
        }
    } catch (ParseException ex) {
        Logger.getLogger(JIFDevolucion.class.getName()).log(Level.SEVERE, null, ex);
    }
    }//GEN-LAST:event_btnGuardarActionPerformed

