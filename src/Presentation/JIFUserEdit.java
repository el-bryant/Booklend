    private void btnEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditarActionPerformed
        txtDni.setEnabled(false);
        txtApellidos.setEnabled(true);
        txtNombres.setEnabled(true);
        txtFnacimiento.setEnabled(true);
        txtTelefono.setEnabled(true);
        txtAddress.setEnabled(true);
        txtCorreo.setEnabled(true);
        txtUser.setEnabled(true);
        psfPassword.setEnabled(true);
        btnEditar.setEnabled(false);
        btnGuardar.setEnabled(true);
        txtDni.requestFocus();
        try {
            String sql = "SELECT usuario.pass FROM usuario WHERE usuario.usu_dni = '" + txtDni.getText() + "'";
            PreparedStatement ps = cnn.getCnn().prepareStatement(sql);
            ResultSet rs = ps.executeQuery();
            String datos = new String();
            while (rs.next()) {
                datos = rs.getString(0);
            }
            psfPassword.setText(datos);
            rs.close();
        } catch (SQLException e) {
            System.out.println(e.getMessage());
        }
    }//GEN-LAST:event_btnEditarActionPerformed

    private void txtAddressKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtAddressKeyTyped
        char caracter = evt.getKeyChar();
        if (Character.isLowerCase(caracter)) {
            String cad = ("" + caracter).toUpperCase();
            caracter = cad.charAt(0);
            evt.setKeyChar(caracter);
        }
    }//GEN-LAST:event_txtAddressKeyTyped

