    private void txtDniKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtDniKeyPressed
        if (evt.getKeyChar() == KeyEvent.VK_ENTER) {
            txtApellidos.requestFocus();
            try {
            String sql = "SELECT usuario.apellidos, usuario.nombres, to_char(usuario.fnacimiento, 'YYYY/MM/DD') as f_nacimiento, usuario.telefono, usuario.direccion, usuario.correo, usuario.login, usuario.pass FROM usuario WHERE usuario.usu_dni = '" + txtDni.getText() + "'";
            PreparedStatement ps = cnn.getCnn().prepareStatement(sql);
            ResultSet rs = ps.executeQuery();
            String datos[] = new String[7];
            while (rs.next()) {
                for (int i = 0; i < 7; i++) {
                    datos[i] = rs.getString(i + 1);
                }
            }
            txtApellidos.setText(datos[0]);
            txtNombres.setText(datos[1]);
            txtFnacimiento.setText(datos[2]);
            txtTelefono.setText(datos[3]);
            txtAddress.setText(datos[4]);
            txtCorreo.setText(datos[5]);
            txtUser.setText(datos[6]);
            psfPassword.setText(datos[7]);
            rs.close();
        } catch (SQLException e) {
            System.out.println(e.getMessage());
        }
        }
        if (txtDni.getText().length() < 8) {
            evt.consume();
        }
    }//GEN-LAST:event_txtDniKeyPressed

