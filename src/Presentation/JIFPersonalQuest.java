    private void setFilas() {
        try {
            String sql = "SELECT personal.pers_dni, personal.apellidos, personal.nombres, personal.login, cargo.nombre FROM personal, cargo WHERE personal.carg_id = cargo.carg_id AND (personal.pers_dni = '" + txtDni.getText() + "' OR personal.apellidos = '" + txtApellidos.getText() + "' OR personal.nombres = '" + txtNombres.getText() + "' OR personal.login = '" + txtLogin.getText() + "')";
            PreparedStatement ps = cnn.getCnn().prepareStatement(sql);
            ResultSet rs = ps.executeQuery();
            Object datos[] = new Object[5];
            while (rs.next()) {
                for (int i = 0; i < 5; i++) {
                    datos[i] = rs.getString(i + 1);
                }
                modelo.addRow(datos);
            }
            rs.close();
        } catch (SQLException e) {
            System.out.println(e.getMessage());
        }
    }

