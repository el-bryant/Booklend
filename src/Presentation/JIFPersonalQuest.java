    private void txtApellidosKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtApellidosKeyTyped
        char caracter = evt.getKeyChar();
        if (caracter != '\b' && (!Character.isLetter(caracter)) && caracter != KeyEvent.VK_SPACE) {
            evt.consume();
        } else {
            if (Character.isLowerCase(caracter)) {
                String cad = ("" + caracter).toUpperCase();
                caracter = cad.charAt(0);
                evt.setKeyChar(caracter);
            }
        }
    }//GEN-LAST:event_txtApellidosKeyTyped

    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
        if (txtDni.getText().equals("") && txtApellidos.getText().equals("") && txtNombres.getText().equals("") && txtLogin.getText().equals("")) {
            JOptionPane.showMessageDialog(this, "Debe llenar al menos un campo para buscar", "AVISO", JOptionPane.INFORMATION_MESSAGE);
            txtDni.selectAll();
            txtApellidos.setText("");
            txtNombres.setText("");
            txtLogin.setText("");
            txtDni.requestFocus();
        } else {
            if (personalshow == null || personalshow.isClosed()) {
                personalshow = new JIFPersonalShow();
                Main.dtpMain.add(personalshow);
                Dimension ds = Main.dtpMain.getSize();
                Dimension fs = personalshow.getSize();
                personalshow.setLocation((ds.width - fs.width) / 2, (ds.height - fs.height) / 2);
                dispose();
                modelo = new DefaultTableModel(null, getColumnas());
                setFilas();
                personalshow.show();
                personalshow.tbPersonal.setModel(modelo);
            }
        }
    }//GEN-LAST:event_btnBuscarActionPerformed

